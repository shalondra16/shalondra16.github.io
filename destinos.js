// Rellenar el menú de continentes
var continentSelect = document.getElementById('continent');
Object.keys(continentCountryInfo).forEach(function(continent) {
    var option = document.createElement('option');
    option.value = continent;
    option.textContent = continent;
    continentSelect.appendChild(option);
});

// Manejar el cambio en el menú de continentes
continentSelect.addEventListener('change', function() {
    var continent = this.value;
    var countrySelect = document.getElementById('country');
    var img = document.getElementById('img');
    var countryInfo = document.getElementById('country-info');

    // Limpiar el menú de países
    countrySelect.innerHTML = '<option value="">Seleccione un país</option>';
    if (continent) {
        // Habilitar y llenar el menú de países
        countrySelect.disabled = false;
        Object.keys(continentCountryInfo[continent]).forEach(function(country) {
            if (country !== 'image') {
                var option = document.createElement('option');
                option.value = country;
                option.textContent = country;
                countrySelect.appendChild(option);
            }
        });

        // Mostrar la imagen del continente
        img.src = continentCountryInfo[continent].image;
        img.style.display = 'block';
    } else {
        // Deshabilitar el menú de países y ocultar la imagen
        countrySelect.disabled = true;
        img.style.display = 'none';
        countryInfo.textContent = 'Selecciona un país para ver la información.';
    }
});

// Manejar el cambio en el menú de países
document.getElementById('country').addEventListener('change', function() {
    var continent = document.getElementById('continent').value;
    var country = this.value;
    var countryInfo = document.getElementById('country-info');

    if (continent && country) {
        // Mostrar la información del país
        countryInfo.textContent = continentCountryInfo[continent][country].info;
    } else {
        countryInfo.textContent = 'Selecciona un país para ver la información.';
    }
});
